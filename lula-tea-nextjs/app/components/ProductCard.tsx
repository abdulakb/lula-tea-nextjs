"use client";

import Image from "next/image";
import { useState } from "react";
import { useLanguage } from "@/context/LanguageContext";
import { useCart } from "@/context/CartContext";
import { useAnalytics } from "@/context/AnalyticsContext";
import { product } from "@/lib/productData";

interface ProductCardProps {
  showActions?: boolean;
}

export default function ProductCard({ showActions = true }: ProductCardProps) {
  const [quantity, setQuantity] = useState(1);
  const { language, t } = useLanguage();
  const { addItem } = useCart();
  const { trackEvent } = useAnalytics();

  const handleAddToCart = () => {
    addItem(
      {
        id: product.id,
        name: product.name,
        nameAr: product.nameAr,
        price: product.price,
        image: product.image,
      },
      quantity
    );
    
    // Track add to cart event
    trackEvent("add_to_cart", {
      product_id: product.id,
      product_name: product.name,
      quantity,
      price: product.price,
      total_value: product.price * quantity,
    });
    
    // Show toast notification (placeholder)
    alert(language === "ar" ? "تمت الإضافة إلى السلة!" : "Added to cart!");
  };

  const handleBuyNow = () => {
    handleAddToCart();
    window.location.href = "/checkout";
  };

  return (
    <div className="bg-warm-cream rounded-3xl shadow-xl overflow-hidden">
      <div className="grid grid-cols-1 md:grid-cols-2 gap-0">
        {/* Product Image */}
        <div className="relative h-80 md:h-auto">
          <Image
            src={product.image}
            alt={language === "ar" ? product.nameAr : product.name}
            fill
            className="object-cover hover:scale-105 transition-transform duration-300"
          />
        </div>

        {/* Product Details */}
        <div className="p-8 md:p-12 flex flex-col justify-center">
          <h3 className="text-3xl font-bold text-deep-brown mb-4">
            {language === "ar" ? product.nameAr : product.name}
          </h3>
          <p className="text-tea-brown mb-6">
            {language === "ar" ? product.descriptionAr : product.description}
          </p>

          <div className="mb-6">
            <div className="flex items-center gap-2 mb-3">
              <svg className="w-5 h-5 text-tea-green" fill="currentColor" viewBox="0 0 20 20">
                <path
                  fillRule="evenodd"
                  d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z"
                  clipRule="evenodd"
                />
              </svg>
              <span className="text-deep-brown">{t("feature1")}</span>
            </div>
            <div className="flex items-center gap-2 mb-3">
              <svg className="w-5 h-5 text-tea-green" fill="currentColor" viewBox="0 0 20 20">
                <path
                  fillRule="evenodd"
                  d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z"
                  clipRule="evenodd"
                />
              </svg>
              <span className="text-deep-brown">{t("feature2")}</span>
            </div>
            <div className="flex items-center gap-2">
              <svg className="w-5 h-5 text-tea-green" fill="currentColor" viewBox="0 0 20 20">
                <path
                  fillRule="evenodd"
                  d="M16.707 5.293a1 1 0 010 1.414l-8 8a1 1 0 01-1.414 0l-4-4a1 1 0 011.414-1.414L8 12.586l7.293-7.293a1 1 0 011.414 0z"
                  clipRule="evenodd"
                />
              </svg>
              <span className="text-deep-brown">{t("feature3")}</span>
            </div>
          </div>

          <div className="mb-6">
            <div className="text-3xl font-bold text-deep-brown mb-1">
              {language === "ar" 
                ? `${(product.price * quantity).toFixed(0)} ريال` 
                : `${(product.price * quantity).toFixed(0)} ${product.currency}`}
            </div>
            <p className="text-sm text-tea-brown/70">
              {language === "ar" 
                ? `${product.price} ريال للعلبة • ٢٥٠ جرام لكل علبة`
                : `${product.price} ${product.currency} per pack • 250g per pack`}
            </p>
          </div>

          {showActions && (
            <>
              {/* Quantity Selector */}
              <div className="mb-6">
                <label className="block text-sm font-medium text-deep-brown mb-2">
                  {t("quantity")}
                </label>
                <div className="flex items-center gap-4">
                  <button
                    onClick={() => setQuantity(Math.max(1, quantity - 1))}
                    className="w-10 h-10 rounded-full bg-tea-green/20 hover:bg-tea-green/30 text-deep-brown font-bold transition-colors"
                  >
                    -
                  </button>
                  <span className="text-2xl font-semibold text-deep-brown w-12 text-center">
                    {quantity}
                  </span>
                  <button
                    onClick={() => setQuantity(quantity + 1)}
                    className="w-10 h-10 rounded-full bg-tea-green/20 hover:bg-tea-green/30 text-deep-brown font-bold transition-colors"
                  >
                    +
                  </button>
                </div>
              </div>

              {/* CTA Buttons */}
              <div className="space-y-3">
                <button
                  onClick={handleAddToCart}
                  className="w-full bg-tea-green hover:bg-tea-green/90 text-white px-6 py-4 rounded-full font-semibold transition-all shadow-lg hover:shadow-xl"
                >
                  {t("addToCart")}
                </button>
                <button
                  onClick={handleBuyNow}
                  className="w-full bg-[#25D366] hover:bg-[#22c55e] text-white px-6 py-4 rounded-full font-semibold transition-all flex items-center justify-center gap-2"
                >
                  <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
                    <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z" />
                  </svg>
                  {t("orderViaWhatsApp")}
                </button>
              </div>
            </>
          )}
        </div>
      </div>
    </div>
  );
}
